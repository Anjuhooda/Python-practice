#### list type of data ####

# append #

x=["apple","banana"]
x.append("mango")
print(x)

# extend #

x=["apple","banana"]
y=["mango","orange"]
x.extend(y)
print(x)

# pop #

x=["apple","banana","mango"]
x.pop()
print(x)

# insert #

x=["apple","banana"]
x.insert(1,"mango")
print(x)

# remove #

x=["apple","banana"]
x.remove("apple")
print(x)

# sort #

x=[5,4,2,6]
x.sort()
print(x)

# clear #

x=["apple","banana","mango"]
x.clear()
print(x)

# copy #

x=["orange","pineapple"]
y=x.copy()
print(y)

